    private void PayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PayButtonActionPerformed
        try {
            String paymentStatus = "YES";
            String orderStatus = "PREPARING";
            DefaultTableModel model = Client.Object.viewOrder(customerID);
            String customerName = model.getValueAt(0, 1).toString();
            int[] itemId = (int[]) model.getValueAt(0,2);
            String[] itemIdAsString = new String[itemId.length];
            for (int i = 0; i < itemId.length; i++) {
                itemIdAsString[i] = String.valueOf(itemId[i]);
            }
            String[] itemName = (String[]) model.getValueAt(0,3);
            double[] priceArray = (double[]) model.getValueAt(0,4);
            int[] quantity = (int[]) model.getValueAt(0, 5);
            int totalQuantity = (int) model.getValueAt(0,6);
            double[] itemTotalPrice = (double[]) model.getValueAt(0, 7);
            double orderTotal = (double) model.getValueAt(0,8);
            String PaymentTime = new SimpleDateFormat("yyyy/MM/dd").format(Calendar.getInstance().getTime());
            String paymentType = (String) selectedPayment.getSelectedItem();
            double paidAmount = Double.parseDouble(jTextField5.getText());  
            double totalPaid = Double.parseDouble(jTextField6.getText());
            double balance = paidAmount - totalPaid;
           
            
            if(totalPaid <= paidAmount){
                Order item = new Order(customerID, customerName, itemIdAsString, itemName, priceArray, itemTotalPrice, quantity, totalQuantity, orderTotal, paymentType, paidAmount, PaymentTime, itemName.length, paymentStatus, orderStatus);
                int receiptId = Client.Object.addOrder(item);
                String formattedBalance = String.format("Balance: RM%.2f", balance); // Formats the balance to two decimal places
                JOptionPane.showMessageDialog(null, formattedBalance);
                new CustomerInvoice(receiptId).setVisible(true);
                dispose();
            } else{
                JOptionPane.showMessageDialog(null, "You're payment is invalid!");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_PayButtonActionPerformed


    private void loadTable(){
        try {
            DefaultTableModel model = Client.Object.viewOrder(customerID);
            lblUname.setText(model.getValueAt(0, 1).toString());
            jTextField2.setText(Arrays.toString((String[])model.getValueAt(0, 3)).replaceAll("\\[|\\]", ""));
            jTextField3.setText(model.getValueAt(0, 6).toString());      
            jTextField4.setText(model.getValueAt(0, 8).toString());
            String total = jTextField4.getText();
            double totalamount = Double.parseDouble(total);
            
            //Multithreading
            double amountServiceTax = Client.Object.serviceTax(totalamount);
//            String formattedAmountServiceTax = String.format("%.2f", amountServiceTax);
            
            
            double amountServiceCharge = Client.Object.serviceCharge(totalamount);
//            String formattedAmountServiceCharge = String.format("%.2f", amountServiceCharge);
            
            jTextField5.setText("0");
            double totalAmount = totalamount + amountServiceTax + amountServiceCharge;
//            String total_amount = Double.toString(totalAmount);
            String formattedTotalAmount = String.format("%.2f", totalAmount);
            jTextField6.setText(formattedTotalAmount);
            
        } catch (Exception ex) {
            ex.printStackTrace();
        // Handle any exceptions that might occur
        }
    }
    
